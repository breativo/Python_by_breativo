# Operadores aritmétricos

## suma(+)
print(2+2) # 4

## resta(-)
print(2-2) # 0

## multiplicación (*)
print(2*5) # 10

## división (/)
print(2/1) # 1

## resto (%)
print( 7%3) # 1

## potencia (**)
print(2**3) # 8

## división, con resultado de número entero
print(18//5) # 3




